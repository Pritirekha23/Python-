#4 LOGICAL OPERATOR
#we can apply for boolean and non-boolean types
#boolean type
#1 and
print("and")
print(True and True)
print(True and False)
print(False and True)
print(False and False)
#2 or
print("or")
print(True or True)
print(True or False)
print(False or True)
print(False or False)
#3 not
print("not")
print(not True)
print(not False)
#NON-BOOLEAN TYPE
#1 and
print("non-boolean and")
print(10 and 27)
print(0 and 12)
print('surendra' and 'surendra panda')
#2 or
print("non-boolean or")
print(10 or 27)
print(0 or 12)
print('surendra' or 'surendra panda')
#3
print("non-boolean not")
print(not 'surendra')
print(not '')