# final story of *args and **kwargs

# Both are used to pass variable length argument.
#Both are used to provide flexibility to the programmer.

# *args  is used to pass  variable length non-keyword argument.
# *args internally stores data in the form of tuple.

# *args should come before **kwargs

# **kwargs  is used to pass  variable length keyword argument.
# **kwargs internally stores data in the form of dict,bcz here we are passing key values.
# *args and **kwargs are both are just naming  convention,that means if we want we can use any name.